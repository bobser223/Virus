what = input ( 'що робимо')

a = int(input ( 'введіть перше число'))
b = int(input ( 'введіть друге число' ))

if what == "+":
    c = (a + b)
    print(c)

elif what == "-":
    c = (a - b)
    print(c)

elif what == "*":
    c = (a * b)
    print(c)

elif what == "/":
    c = (a / b)
    print(c)
